def perform_frequency_test(data, label):
    total = len(data)
    expected = total / 5
    counts = Counter(data)
    chi_sq = sum(((counts[i] - expected) ** 2) / expected for i in range(1, 6))

    print(f"--- {label} ---")
    print(f"Chi-Square Statistic: {chi_sq:.4f}")
    for i in range(1, 6):
        print(f"  Number {i}: {(counts[i]/total)*100:.1f}%")


# --- TEST 2: TRANSITION MATRIX ---
def get_transition_matrix(data):
    matrix = np.zeros((5, 5))
    for i in range(len(data) - 1):
        matrix[data[i] - 1, data[i + 1] - 1] += 1
    row_sums = matrix.sum(axis=1)
    return matrix / row_sums[:, np.newaxis]


