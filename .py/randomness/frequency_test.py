def perform_frequency_test(data, label):
    total = len(data)
    expected = total / 5  # We expect 20% for each of the 5 numbers
    counts = Counter(data)

    # Calculate Chi-Square Statistic: sum((Observed - Expected)^2 / Expected)
    chi_sq = sum(((counts[i] - expected) ** 2) / expected for i in range(1, 6))

    print(f"--- {label} Frequency Test ---")
    print(f"Total elements: {total}")
    for i in range(1, 6):
        percentage = (counts[i] / total) * 100
        print(f"Number {i}: {percentage:.2f}%")
    print(f"Chi-Square Statistic: {chi_sq:.4f}\n")


